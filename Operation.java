
public class Operation {

	private String name;
	private CustomNode requestNode;
	private CustomNode responseNode;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public CustomNode getRequestNode() {
		return requestNode;
	}
	public void setRequestNode(CustomNode requestNode) {
		this.requestNode = requestNode;
	}
	public CustomNode getResponseNode() {
		return responseNode;
	}
	public void setResponseNode(CustomNode responseNode) {
		this.responseNode = responseNode;
	}
	
	
}
